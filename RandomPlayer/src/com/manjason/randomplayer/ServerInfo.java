package com.manjason.randomplayer;


public class ServerInfo {

    private String name;
    private int players;


    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPlayers() {
        return this.players;
    }

    public void setPlayers(int players) {
        this.players = players;
    }
}
